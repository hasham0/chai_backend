import { Schema, model } from "mongoose";
import mongooseAggregatePaginate from "mongoose-aggregate-paginate-v2";

const videoSchema = new Schema(
  {
    videoFile: {
      type: String, //! cloudinary url_
      required: true,
    },
    thumbnail: {
      type: String, //! cloudinary url_
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    duration: {
      type: Number, //! cloudinary url_
      required: true,
    },
    views: {
      type: Number,
      default: 0,
    },
    isPublished: {
      type: Boolean,
      default: true,
    },
    owner: {
      type: Schema.Types.ObjectId,
      ref: "User",
    },
  },
  {
    timestamps: true,
  }
);

/* inject mongoose aggregate paginate */
videoSchema.plugin(mongooseAggregatePaginate);

const Video = model["Video"] || model("Video", videoSchema);

export default Video;
